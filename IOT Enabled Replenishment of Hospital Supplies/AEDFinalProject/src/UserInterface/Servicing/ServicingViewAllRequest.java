    public void populateworkRequestJTable() {

        DefaultTableModel model = (DefaultTableModel)workRequestJTable.getModel();
        model.setRowCount(0);
        for(WorkRequest request : servicingOrganization.getWorkQueue().getWorkRequestList())
        {
            if(!request.getStatus().equalsIgnoreCase("Serviced"))
            {
            Object[] row = new Object[10];
            row[0] = request;
            row[1] = request.getMsgforServicing();
            row[2] = request.getReceiver();
            row[3] = request.getStatus();
            row[4] = request.getSender();
            row[5] = request.getOrg();
            row[6] = request.getMachine();
            row[7] = request.getSensorStatusOld();
            row[8] = request.getRequestDate();
            row[9] = request.getResolveDate();
            model.addRow(row);
            }
        }
    }
