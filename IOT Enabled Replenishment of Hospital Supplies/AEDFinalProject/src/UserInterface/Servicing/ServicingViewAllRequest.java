    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int selectedRow = workRequestJTable.getSelectedRow();
        if(selectedRow>=0)
        {
            WorkRequest request = (WorkRequest)workRequestJTable.getValueAt(selectedRow, 0);
            ServicingProcessRequestJPanel ServicingProcessRequestJPanel = new ServicingProcessRequestJPanel(userProcessContainer,enterprise,servicingOrganization,request);
            userProcessContainer.add("ProcessRequestJPanel", ServicingProcessRequestJPanel);
            CardLayout layout = (CardLayout) userProcessContainer.getLayout();
            layout.next(userProcessContainer);
        }
        else
        {
            JOptionPane.showMessageDialog(null,"Kindly select Any Row");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

