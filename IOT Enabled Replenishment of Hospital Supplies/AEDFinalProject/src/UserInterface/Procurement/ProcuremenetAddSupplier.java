    private void populateSupplier() {
        
        DefaultTableModel model = (DefaultTableModel)AddSupplierJTable.getModel();
        model.setRowCount(0);
        
        for(Supplier s: organization.getSupplierDirectory().getSuppplierList())
        {
            Object[] row = new Object[2];
            row[0] = s.getSupplierID();
            row[1] = s;
            model.addRow(row);
        }
    }
    
