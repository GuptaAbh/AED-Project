    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        
        int selectedRow = AddSupplierJTable.getSelectedRow();
        if(selectedRow>=0)
            {
        Supplier supplier = (Supplier)AddSupplierJTable.getValueAt(selectedRow, 1);
        
        
        ProcurementAddProduct papJPanel = new ProcurementAddProduct(userProcessContainer,supplier);
        userProcessContainer.add("ProcurementAddProduct", papJPanel);
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.next(userProcessContainer);
            }
        else
            {
                JOptionPane.showMessageDialog(null,"Kindly Select a Row");
            }
        
        
        
    }//GEN-LAST:event_jButton2ActionPerformed

