package Business.Employee;

/**
 *
 * @author Abhishek
 */
public class Employee {
    
    private String name;
    private static int count = 1;
    private int id;
    
    
    public Employee() {
        id = count;
        count++;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    @Override
    public String toString() {
        return name;
    }
    
    
    
    
    
}
